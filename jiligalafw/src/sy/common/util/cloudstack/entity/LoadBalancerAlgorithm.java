package sy.common.util.cloudstack.entity;

/**
 * 
 * @author lidongbo
 * 
 */
public enum LoadBalancerAlgorithm {

	roundrobin("roundrobin", "轮询算法"),
	leastconn("leastconn", "最少连接算法"),
	source("source", "源算法")
	;
	
	public final String CODE;

	public final String NAME;

	private LoadBalancerAlgorithm(String code, String name) {
		this.CODE = code;
		this.NAME = name;
	}

}
