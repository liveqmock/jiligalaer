package sy.domain.model.product;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import sy.domain.model.shared.CloudPersistentObject;

/**
 * 模板表
 * @author CS
 *
 */
@Entity
@Table(name = "CLOUD_MDM_TEMPLATE")
public class CloudMdmTemplate extends CloudPersistentObject {

	/**
	 * 
	 */
	private static final long serialVersionUID = -3624974548758505616L;

	/**
	 * 名称.
	 */
	private String name;
	/**
	 * 显示的数据中心信息.
	 */
	private String displayText;
	/**
	 * 内存
	 */
	private String  memory;
	
	private Date created;
	private String isready;
	private String ostypeid;
	private String ostypename;
	private String zoneid;
	private String status;
	private String size;
	private String hypervisor;
	private String isextractable;
	

	@Column(name="NAME")
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Column(name="DISPLAYTEXT")
	public String getDisplayText() {
		return displayText;
	}

	public void setDisplayText(String displayText) {
		this.displayText = displayText;
	}

	@Column(name="MEMORY")
	public String getMemory() {
		return memory;
	}

	public void setMemory(String memory) {
		this.memory = memory;
	}

	@Column
	public Date getCreated() {
		return created;
	}

	public void setCreated(Date created) {
		this.created = created;
	}

	@Column
	public String getIsready() {
		return isready;
	}

	public void setIsready(String isready) {
		this.isready = isready;
	}

	@Column
	public String getOstypeid() {
		return ostypeid;
	}

	public void setOstypeid(String ostypeid) {
		this.ostypeid = ostypeid;
	}

	@Column
	public String getOstypename() {
		return ostypename;
	}

	public void setOstypename(String ostypename) {
		this.ostypename = ostypename;
	}

	@Column
	public String getZoneid() {
		return zoneid;
	}

	public void setZoneid(String zoneid) {
		this.zoneid = zoneid;
	}

	@Column
	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	@Column
	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}

	@Column
	public String getHypervisor() {
		return hypervisor;
	}

	public void setHypervisor(String hypervisor) {
		this.hypervisor = hypervisor;
	}

	@Column
	public String getIsextractable() {
		return isextractable;
	}

	public void setIsextractable(String isextractable) {
		this.isextractable = isextractable;
	}

	
}
