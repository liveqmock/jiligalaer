package sy.domain.vo.product;

import sy.common.util.cloudstack.entity.Serviceoffering;

/**
 * CPU计算方案表.
 * 
 * @author CS
 * 
 */
public class CloudMdmCPUSolnVo extends CloudDataSyncBuilder<Serviceoffering> {

	private static final long serialVersionUID = 6615366709143182939L;


	/**
	 * CPU名称.
	 */
	private String name;
	/**
	 * 显示的描述信息.
	 */
	private String displayText;
	/**
	 * CPU数量.
	 */
	private String cpuNumber;
	/**
	 * CPU频率
	 */
	private String cpuSpeed;
	/**
	 * 内存
	 */
	private String memory;
	/**
	 * 选择状态标志
	 */
    private String selectFlag;
    
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDisplayText() {
		return displayText;
	}

	public void setDisplayText(String displayText) {
		this.displayText = displayText;
	}

	public String getCpuNumber() {
		return cpuNumber;
	}

	public void setCpuNumber(String cpuNumber) {
		this.cpuNumber = cpuNumber;
	}

	public String getCpuSpeed() {
		return cpuSpeed;
	}

	public void setCpuSpeed(String cpuSpeed) {
		this.cpuSpeed = cpuSpeed;
	}

	public String getMemory() {
		return memory;
	}

	public void setMemory(String memory) {
		this.memory = memory;
	}

	public String getSelectFlag() {
		return selectFlag;
	}

	public void setSelectFlag(String selectFlag) {
		this.selectFlag = selectFlag;
	}


	

}
