package sy.domain.vo.product;

import java.util.Date;

import sy.common.util.valuesetter.Create;
import sy.domain.vo.shared.BaseVo;

/**
 * 产品管理-产品主表VO
 * 
 * @author luobin
 * 
 */
public class CloudMdmProductVo extends BaseVo {

	private static final long serialVersionUID = 6615363709143182939L;

	private String id;
	
	/**
	 * 产品名称
	 */
	private String prodName;
	/**
	 * 产品描述
	 */
	private String description;
	/**
	 * 产品类型
	 */
	private String prodType;
	
	@Create
	private Date created;
	
	private Integer state;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getProdName() {
		return prodName;
	}

	public void setProdName(String prodName) {
		this.prodName = prodName;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getProdType() {
		return prodType;
	}

	public void setProdType(String prodType) {
		this.prodType = prodType;
	}

	public Date getCreated() {
		return created;
	}

	public void setCreated(Date created) {
		this.created = created;
	}

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

}
