package sy.domain.vo.product;

import sy.domain.vo.shared.BaseVo;

/**
 * 产品价格信息VO
 * 
 * @author luobin
 * 
 */
public class MdmProductPriceInfo extends BaseVo {

	private static final long serialVersionUID = 6615363709143182939L;

	/**
	 * 价格ID
	 */
	private String priceId;
	/**
	 * 产品ID
	 */
	private  String prodId;
	/**
	 * 产品名称
	 */
	private String prodName;
	/**
	 * 价格
	 */
	private Double price;
	/**
	 * 价格方式 （一次性、周期性）
	 */
	private String priceMode;
	/**
	 * 价格周期（年，月）
	 */
	private String priceCycle;
	/**
	 * 生效日期
	 */
	private String effectiveDate;
	
	
	public String getPriceId() {
		return priceId;
	}
	public void setPriceId(String priceId) {
		this.priceId = priceId;
	}
	public String getProdId() {
		return prodId;
	}
	public void setProdId(String prodId) {
		this.prodId = prodId;
	}
	public String getProdName() {
		return prodName;
	}
	public void setProdName(String prodName) {
		this.prodName = prodName;
	}
	public Double getPrice() {
		return price;
	}
	public void setPrice(Double price) {
		this.price = price;
	}
	public String getPriceMode() {
		return priceMode;
	}
	public void setPriceMode(String priceMode) {
		this.priceMode = priceMode;
	}
	public String getPriceCycle() {
		return priceCycle;
	}
	public void setPriceCycle(String priceCycle) {
		this.priceCycle = priceCycle;
	}
	public String getEffectiveDate() {
		return effectiveDate;
	}
	public void setEffectiveDate(String effectiveDate) {
		this.effectiveDate = effectiveDate;
	}
	
}
